// Java program to count bits in binary number representation
package com.countbits;

// Main class
public class Main {

    // Main method to run java program
    public static void main(String[] args) {

        // Printing how many 1s in binary number 8
        System.out.println("Bits in number 8: " + Integer.bitCount(8));

        // Printing how many 1s in binary number 17
        System.out.println("Bits in number 17: " + Integer.bitCount(17));

        // Printing how many 1s in binary number 21
        System.out.println("Bits in number 21: " + Integer.bitCount(21));

        // Printing how many 1s in binary number 27
        System.out.println("Bits in number 27: " + Integer.bitCount(27));

    }
}